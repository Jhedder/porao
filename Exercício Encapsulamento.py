class Pessoa:
    def __init__(self, nome, idade):
        self.nome = nome
        self.idade = idade

    def mostra_nome(self):
        print(f"O nome desta pessoa é: {self.nome}.")

    def mostra_idade(self):
        print(f"A idade desta pessoa é: {self.idade}.")

pessoa1 = Pessoa("Jhedder",20)
pessoa1.mostra_nome()
pessoa1.mostra_idade()

pessoa2 = Pessoa("Duda",20)
pessoa2.mostra_nome()
pessoa2.mostra_idade()






        
    
       
